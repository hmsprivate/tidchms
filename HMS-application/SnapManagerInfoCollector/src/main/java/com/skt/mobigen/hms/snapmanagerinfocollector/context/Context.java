package com.skt.mobigen.hms.snapmanagerinfocollector.context;

import org.apache.ibatis.session.SqlSessionFactory;

public class Context {

	private SqlSessionFactory sqlSessionFactory;
	private String snap_manager_task_dir;
	private String snap_manager_plugin_dir;
	private String snap_manager_port;
	private String process_interval_time;

	public String getProcess_interval_time() {
		return process_interval_time;
	}

	public void setProcess_interval_time(String process_interval_time) {
		this.process_interval_time = process_interval_time;
	}

	public String getSnap_manager_port() {
		return snap_manager_port;
	}

	public void setSnap_manager_port(String snap_manager_port) {
		this.snap_manager_port = snap_manager_port;
	}

	public SqlSessionFactory getSqlSessionFactory() {
		return sqlSessionFactory;
	}

	public void setSqlSessionFactory(SqlSessionFactory sqlSessionFactory) {
		this.sqlSessionFactory = sqlSessionFactory;
	}

	public String getSnap_manager_task_dir() {
		return snap_manager_task_dir;
	}

	public void setSnap_manager_task_dir(String snap_manager_task_dir) {
		this.snap_manager_task_dir = snap_manager_task_dir;
	}

	public String getSnap_manager_plugin_dir() {
		return snap_manager_plugin_dir;
	}

	public void setSnap_manager_plugin_dir(String snap_manager_plugin_dir) {
		this.snap_manager_plugin_dir = snap_manager_plugin_dir;
	}

}
